using System;

class Program
//Юхневич Практика
{
    static void Main(string[] args)
    {
        // Просим пользователя ввести число для вычисления факториала
        Console.WriteLine("Введите число для вычисления факториала:");
        int number;

        // Проверяем, является ли введенное значение числом, и оно должно быть неотрицательным
        while (!int.TryParse(Console.ReadLine(), out number) || number < 0)
        {
            // Если введенное значение не корректно, просим ввести снова
            Console.WriteLine("Пожалуйста, введите неотрицательное целое число.");
        }

        // Вычисляем факториал введенного числа
        long factorial = CalculateFactorial(number);

        // Выводим результат на экран
        Console.WriteLine($"Факториал {number} равен {factorial}");
    }

    // Метод для вычисления факториала числа
    static long CalculateFactorial(int n)
    {
        // Если число равно 0 или 1, возвращаем 1 (так как факториал 0! и 1! равен 1)
        if (n == 0 || n == 1)
            return 1;

        // Переменная для хранения результата факториала
        long result = 1;

        // Цикл для последовательного умножения всех чисел от 2 до n
        for (int i = 2; i <= n; i++)
        {
            result *= i; // Умножаем результат на текущее значение i
        }

        // Возвращаем результат
        return result;
    }
}
